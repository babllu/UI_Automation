package A_Bascis;

public class F_Do_While_Loop {

	public static void main(String[] args) {
		
		int i=1;             //variable initialization
//		In do while loop: statement in side the do block will executes first.
//		After executing the statement it condition is checked.
/*		1. If the given condition is true, loop repeates
		2. if the conditio is false, loop terminates and it will continues the execute in the loop body at once.*/
		do {  
			
			System.out.println("Hi Abhishek");   //loop body
			i++;
		}while(i<=4);  //condition

	}

}
